package id.ac.itb.indoormap.model;

public class Posisi {
	
	private double x;
	private double y;
	private int id_denah;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public int getId_denah() {
		return id_denah;
	}
	public void setId_denah(int id_denah) {
		this.id_denah = id_denah;
	}
	
}
